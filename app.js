function escapeHtml(value) {
  return String(value || "")
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;")
    .replaceAll("\n", "<br>");
}

function escapeAttribute(value) {
  return String(value || "")
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}

function escapeTextareaValue(value) {
  return escapeAttribute(value);
}

function formatDateLabel(value) {
  if (!value) return "확인 중";
  const [year, month, day] = String(value).split("-");
  if (!year || !month || !day) return String(value);
  return `${year}.${month}.${day}`;
}

function showToast(message) {
  const toast = $("#toast");
  toast.textContent = message;
  toast.classList.add("show");
  window.setTimeout(() => toast.classList.remove("show"), 2200);
}

function initRecommendationOnly(defaultMode = "advanced") {
  state.recommendationMode = defaultMode;
  bindRecommendationControls();
  renderRecommendationExplorer();
}

document.addEventListener("DOMContentLoaded", () => {
  if ($("#home")) {
    init();
    return;
  }

  if ($("#advancedRecommendationPage")) {
    initRecommendationOnly("advanced");
  }
});
