function addAdditionalCourseToPlan(grade, semester, form) {
  const semesterPlan = state.plan[grade]?.[semester];
  if (!semesterPlan) return;
  if (!Array.isArray(semesterPlan.additional)) semesterPlan.additional = [];

  if (semesterPlan.additional.length >= 2) {
    showToast("추가 교육과정은 학기별 최대 2과목까지 입력합니다.");
    return;
  }

  const nameInput = form.querySelector("[data-additional-course-name]");
  const reasonInput = form.querySelector("[data-additional-course-reason]");
  const selectedCourse = getAdditionalCourseByName(nameInput?.value || "");
  const reason = String(reasonInput?.value || "").trim();

  if (!selectedCourse) {
    showToast("목록에서 과목명을 정확히 선택해 주세요.");
    return;
  }

  if (!reason) {
    showToast("그 과목이 필요한 이유를 한 문장으로 적어 주세요.");
    return;
  }

  if (semesterPlan.additional.some((item) => normalizeText(item.name) === normalizeText(selectedCourse.name))) {
    showToast("이미 이 학기에 추가한 과목입니다.");
    return;
  }

  semesterPlan.additional.push({
    name: selectedCourse.name,
    area: selectedCourse.area,
    category: selectedCourse.category,
    reason
  });

  saveState(false);
  renderPlannerMineView();
  renderPlannerSavedSummary();
}

function removeAdditionalCourseFromPlan(grade, semester, index) {
  const additional = state.plan[grade]?.[semester]?.additional;
  if (!Array.isArray(additional) || !additional[index]) return;
  additional.splice(index, 1);
  saveState(false);
  renderPlannerMineView();
  renderPlannerSavedSummary();
}

function addCourseToPlan(courseId, grade, semester, zone) {
  const course = getPlannerCourse(courseId);
  if (!course) return;
  const semesterKey = `${grade}-${semester}`;

  if (course.semesters?.length && !course.semesters.includes(semesterKey)) {
    showToast("이 과목은 해당 학기에 편성된 과목이 아닙니다.");
    return;
  }

  const list = state.plan[grade][semester][zone];
  if (list.some((item) => item.id === course.id)) {
    showToast("이미 이 학기에 추가한 과목입니다.");
    return;
  }

  const actionState = getPlannerCourseActionState(course, grade, semester);
  if (actionState.disabledByLimit) {
    showToast(`이 선택 묶음은 ${actionState.limit}과목까지만 선택할 수 있습니다.`);
    return;
  }

  list.push({ id: course.id, credits: getPlannerCourseCredits(course, grade, semester) });

  const jointCredits = sumCredits(state.plan[grade][semester].joint);
  if (zone === "joint" && jointCredits > 6) {
    list.pop();
    showToast("공동교육과정은 학기당 최대 6학점까지 입력합니다.");
    return;
  }

  saveState(false);
  renderCoursePool();
  renderPlanner();
}

function removeCourseFromPlan(courseId, grade, semester, zone) {
  const semesterPlan = state.plan[grade]?.[semester];
  if (!semesterPlan) return;
  const zones = zone ? [zone] : ["regular", "joint"];
  let removed = false;

  zones.forEach((zoneKey) => {
    const list = semesterPlan[zoneKey] || [];
    const index = list.findIndex((item) => item.id === courseId);
    if (index >= 0) {
      list.splice(index, 1);
      removed = true;
    }
  });

  if (!removed) return;
  saveState(false);
  renderCoursePool();
  renderPlanner();
}

function sumCredits(list) {
  return list.reduce((sum, item) => sum + Number(item.credits || 0), 0);
}

