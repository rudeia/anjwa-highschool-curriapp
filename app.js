function getSelfEvaluationPlan() {
  return curriculumData.plans?.[state.selfEvalPlan] || curriculumData.plans?.incoming2026 || { courses: [] };
}

function getSelfEvaluationCourses() {
  const plan = getSelfEvaluationPlan();
  return (plan.courses || [])
    .filter((course) => course.name)
    .filter((course) => state.selfEvalSemester === "all" || course.semesters?.includes(state.selfEvalSemester))
    .sort((a, b) => getCourseFirstSemesterSort(a) - getCourseFirstSemesterSort(b) || a.row - b.row || a.name.localeCompare(b.name, "ko"));
}

function getCourseFirstSemesterSort(course) {
  const firstSemester = curriculumSemesterOrder.find((semesterKey) => course.semesters?.includes(semesterKey));
  return firstSemester ? getSemesterSortIndex(firstSemester) : 999;
}

function ensureSelfEvaluationSubject() {
  const coursesForPlan = getSelfEvaluationCourses();
  if (!coursesForPlan.length) {
    state.selfEvalSubject = "";
    return;
  }
  if (!coursesForPlan.some((course) => course.id === state.selfEvalSubject)) {
    state.selfEvalSubject = coursesForPlan[0].id;
  }
}

function getSelfEvaluationCourse() {
  ensureSelfEvaluationSubject();
  return getSelfEvaluationCourses().find((course) => course.id === state.selfEvalSubject) || null;
}

function getSelfEvaluationEntryKey() {
  ensureSelfEvaluationSubject();
  return state.selfEvalSubject || "";
}

function createEmptySelfEvaluationEntry() {
  return {
    motivation: "",
    question: "",
    concept: "",
    process: "",
    result: "",
    limit: "",
    followUp: "",
    book: "",
    bookReflection: "",
    bookConnection: "",
    competencies: [],
    competencyNote: ""
  };
}

function getSelfEvaluationEntry() {
  const planKey = state.selfEvalPlan;
  const entryKey = getSelfEvaluationEntryKey();
  if (!planKey || !entryKey) return createEmptySelfEvaluationEntry();
  if (!state.selfEvalEntries[planKey]) state.selfEvalEntries[planKey] = {};
  if (!state.selfEvalEntries[planKey][entryKey]) {
    state.selfEvalEntries[planKey][entryKey] = createEmptySelfEvaluationEntry();
  }
  return state.selfEvalEntries[planKey][entryKey];
}

