function normalizeText(value) {
  return String(value || "")
    .normalize("NFC")
    .replaceAll("Ⅰ", "1")
    .replaceAll("ⅰ", "1")
    .replaceAll("Ⅱ", "2")
    .replaceAll("ⅱ", "2")
    .replaceAll("Ⅲ", "3")
    .replaceAll("ⅲ", "3")
    .toLowerCase()
    .replace(/[^0-9a-z가-힣]/g, "");
}

